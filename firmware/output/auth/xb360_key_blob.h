#pragma once

#include <stdint.h>

// Xbox 360 key blob layout (64 bytes total), loaded from firmware at boot.
// This blob is consumed by XInput auth init.
//
// Offsets:
//   0x00..0x1C : XSM3 identification packet (29 bytes)
//   0x1D..0x1F : reserved
//   0x20..0x2B : serial (12 bytes, optional override)
//   0x2C..0x3F : reserved
#define XB360_KEY_BLOB_SIZE             64
#define XB360_KEY_BLOB_ID_DATA_OFFSET   0x00
#define XB360_KEY_BLOB_ID_DATA_SIZE     0x1D
#define XB360_KEY_BLOB_SERIAL_OFFSET    0x20
#define XB360_KEY_BLOB_SERIAL_SIZE      0x0C

// Enable firmware key blob by default.
#ifndef XB360_KEY_BLOB_ENABLED
#define XB360_KEY_BLOB_ENABLED 1
#endif

#if XB360_KEY_BLOB_ENABLED
static const uint8_t xb360_key_blob_template[XB360_KEY_BLOB_SIZE] = {
  // 0x00..0x1C: identification packet
  0x49, 0x4B, 0x00, 0x00, 0x17, 0x41, 0x41, 0x41,
  0x41, 0x41, 0x41, 0x41, 0x41, 0x41, 0x41, 0x41,
  0x00, 0x00, 0x80, 0x02, 0x09, 0x12, 0x82, 0x28,
  0x03, 0x00, 0x01, 0x01, 0x71,
  // 0x1D..0x1F: reserved
  0xFF, 0xFF, 0xFF,
  // 0x20..0x2B: serial override (optional)
  'A', 'A', 'A', 'A', 'A', 'A', 'A', 'A', 'A', 'A', 'A', 'A',
  // 0x2C..0x3F: reserved
  0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
  0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
  0xFF, 0xFF, 0xFF, 0xFF
};
#endif
